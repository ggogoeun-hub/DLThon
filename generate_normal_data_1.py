# 데이터 조합 및 CSV 저장
# ============================================================
def main():
    all_conversations = []
    rng = random.Random(2024)

    categories = [
        ("일상/날씨", daily_weather),
        ("음식/맛집", food_restaurant),
        ("취미/여가", hobby_leisure),
        ("학교/공부", school_study),
        ("직장/일상", work_daily),
    ]

    idx_counter = 0
    for cat_name, convs in categories:
        for conv in convs:
            # 길이 부족하면 확장
            conv = extend_conversation(conv, cat_name, rng)
            idx_str = f"n_{idx_counter:04d}"
            all_conversations.append({
                "idx": idx_str,
                "class": "일반 대화",
                "conversation": conv,
            })
            idx_counter += 1

    # 검증
    assert len(all_conversations) == 500, f"Expected 500, got {len(all_conversations)}"

    # 대화 길이 검증
    short_count = 0
    long_count = 0
    for item in all_conversations:
        conv_len = len(item["conversation"])
        if conv_len < MIN_LEN:
            short_count += 1
            print(f"[WARN] {item['idx']} too short: {conv_len} chars")
        if conv_len > MAX_LEN:
            long_count += 1
            print(f"[WARN] {item['idx']} too long: {conv_len} chars")

    # 금지 키워드 검증
    forbidden = ["죽", "죽여", "칼", "살려", "내놔", "찐따", "왕따"]
    for item in all_conversations:
        for kw in forbidden:
            if kw in item["conversation"]:
                print(f"[WARN] {item['idx']} contains forbidden keyword: {kw}")

    # CSV 저장
    output_dir = os.path.dirname(os.path.abspath(__file__))
    output_path = os.path.join(output_dir, "normal_conversations_500.csv")

    with open(output_path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=["idx", "class", "conversation"])
        writer.writeheader()
        writer.writerows(all_conversations)

    print(f"\n[SUCCESS] {len(all_conversations)}개 대화 저장 완료: {output_path}")
    print(f"  - 일상/날씨: {len(daily_weather)}개")
    print(f"  - 음식/맛집: {len(food_restaurant)}개")
    print(f"  - 취미/여가: {len(hobby_leisure)}개")
    print(f"  - 학교/공부: {len(school_study)}개")
    print(f"  - 직장/일상: {len(work_daily)}개")
    print(f"  - 100자 미만: {short_count}개")
    print(f"  - 400자 초과: {long_count}개")

    # 길이 통계
    lengths = [len(item["conversation"]) for item in all_conversations]
    print(f"  - 평균 길이: {sum(lengths)/len(lengths):.1f}자")
    print(f"  - 최소: {min(lengths)}자 / 최대: {max(lengths)}자")


if __name__ == "__main__":
    main()
