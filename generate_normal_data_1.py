MIN_LEN = 100
MAX_LEN = 400

def extend_conversation(conv: str, cat_name: str, rng: random.Random) -> str:
    """짧은 대화에 자연스러운 후속 발화를 추가하여 MIN_LEN 이상으로 만듦."""
    pool = _ext_pools.get(cat_name, _ext_daily)
    shuffled = pool[:]
    rng.shuffle(shuffled)
    ptr = 0
    while len(conv) < MIN_LEN and ptr < len(shuffled):
        conv = conv + "\n" + shuffled[ptr]
        ptr += 1
    # MAX_LEN 초과 시 잘라냄 (마지막 완전한 발화까지)
    if len(conv) > MAX_LEN:
        lines = conv.split("\n")
        trimmed = ""
        for line in lines:
            candidate = (trimmed + "\n" + line).strip()
            if len(candidate) <= MAX_LEN:
                trimmed = candidate
            else:
                break
        conv = trimmed
    return conv


